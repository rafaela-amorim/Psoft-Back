package ajude.classesAuxiliares;

public class Quantia {
	
	private double quantidaQueFalta;
	
	public Quantia() {
		super();
	}
	
	public Quantia(double falta) {
		this.quantidaQueFalta = falta;
	}

	public double getQuantidaQueFalta() {
		return quantidaQueFalta;
	}

	public void setQuantidaQueFalta(double quantidaQueFalta) {
		this.quantidaQueFalta = quantidaQueFalta;
	}
	
	
}
